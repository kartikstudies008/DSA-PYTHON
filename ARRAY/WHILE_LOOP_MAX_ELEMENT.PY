# MAXIMUM ELEMENT FIND USING WHILE LOOP !

arr = [4,1,8,6]
maximum = arr[0]   #INITIALIZE THE MAXIMUM VARIABLE TO THE FIRST ELEMENT OF THE ARRAY. THIS ASSUMES THAT THE FIRST ELEMENT IS THE LARGEST UNTIL PROVEN OTHERWISE AS WE ITERATE THROUGH THE ARRAY.
i = 0

while i < len(arr):
    if arr[i] > maximum:  #IF THE CURRENT ELEMENT arr[i] IS GREATER THAN THE CURRENT MAXIMUM, THEN UPDATE THE MAXIMUM VARIABLE TO THIS NEW VALUE. THIS ENSURES THAT BY THE END OF THE LOOP, maximum WILL HOLD THE LARGEST VALUE FOUND IN THE ARRAY.
        maximum = arr[i]
    i+=1
print(maximum)

